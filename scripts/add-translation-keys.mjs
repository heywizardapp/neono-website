function mergeKeys(filePath, newKeys) {
  const existing = JSON.parse(readFileSync(filePath, 'utf-8'));
  const merged = { ...existing, ...newKeys };
  writeFileSync(filePath, JSON.stringify(merged, null, 2) + '\n');
  const added = Object.keys(newKeys).filter(k => !(k in existing));
  const updated = Object.keys(newKeys).filter(k => k in existing && existing[k] !== newKeys[k]);
  console.log(`${filePath}: ${added.length} added, ${updated.length} updated, ${Object.keys(merged).length} total keys`);
}

